CREATE TABLE ULEAPP_FINANCECR.CERTIFICATE_INFO (
  ID NUMBER(11) PRIMARY KEY NOT NULL, 
  BUSI_NAME VARCHAR2(255) NOT NULL  ,
  SYS_NAME VARCHAR2(255) NOT NULL ,
  CER_SEND_TYPE NUMBER(10) NOT NULL  ,
  CER_TYPE NUMBER(10) NOT NULL ,
  EXPIRATION_DATE VARCHAR2(15)  ,
  CHANNEL_CODE VARCHAR2(50) ,
  EMAIL VARCHAR2(50) NOT NULL  ,
  CER_OUT_TYPE NUMBER(10)  ,
  CER_OUT_FILENAME VARCHAR2(250)  ,
  CER_NAME VARCHAR2(50) NOT NULL  ,
  DOWN_PASSWORD VARCHAR2(255)  ,
  REMARK VARCHAR2(255) ,
  CER_ENV NUMBER(10) ,
  CER_CONTENT CLOB ,
  CREATE_TIME TIMESTAMP,
  EDIT_TIME TIMESTAMP  
) TABLESPACE TBS_FINANCECR_DATA;

COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.ID IS '主键';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.BUSI_NAME IS '业务单元名';
