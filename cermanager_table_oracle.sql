COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_SEND_TYPE IS '证书颁发主体 1.邮乐颁发 2.外部颁发';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_TYPE IS '证书类型 1.公钥 2.私钥 3.PFX公私钥';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.EXPIRATION_DATE IS '证书过期时间';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CHANNEL_CODE IS '外部颁发证书的通道编码';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.EMAIL IS 'EMAIL地址';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_OUT_TYPE IS '外部证书类型 1.文件证书 2.秘钥串证书';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_OUT_FILENAME IS '外部证书原始文件名';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_NAME IS '生成的文件证书名(服务器保存)';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.DOWN_PASSWORD IS '访问文件夹服务的密码';

COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.REMARK IS '预留字段';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_ENV IS '证书使用环境 1.开发 2.测试 3.生产';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CER_CONTENT IS '证书内容';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN ULEAPP_FINANCECR.CERTIFICATE_INFO.EDIT_TIME IS '修改时间';

